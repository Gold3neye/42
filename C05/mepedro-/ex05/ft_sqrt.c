int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	while (i * i != nb && i <= 46340)
	{
		if (i * i == nb)
		{
			return (0);
		}
		++i;
	}
	if (i * i == nb)
	{
		return (i);
	}
	else
	{
		return (0);
	}
}
